package LeetcodeProblems;

public class IntegerCaching {

	public static void main(String[] args) {
		
		// Caching range is -128 to 127
		Integer num1 = 128;
		Integer num2 = 128;
		
		if(num1 == num2)
		{
			System.out.println("Both are equal...");
		}
		else {
			System.out.println("Both are not equal...");
		}

	}

}
